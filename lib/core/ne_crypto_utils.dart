  static String md5(String input) {
    final bytes = utf8.encode(input);
    var h0 = 0x67452301;
    var h1 = 0xefcdab89;
    var h2 = 0x98badcfe;
    var h3 = 0x10325476;

    final origLen = bytes.length;
    final padded = List<int>.from(bytes);
    padded.add(0x80);
    while (padded.length % 64 != 56) {
      padded.add(0x00);
    }
    final bitLen = origLen * 8;
    padded.add(bitLen & 0xff);
    padded.add((bitLen >> 8) & 0xff);
    padded.add((bitLen >> 16) & 0xff);
    padded.add((bitLen >> 24) & 0xff);
    for (int i = 4; i < 8; i++) {
      padded.add(0);
    }

    for (int offset = 0; offset < padded.length; offset += 64) {
      final w = List<int>.filled(80, 0);
      for (int i = 0; i < 16; i++) {
        w[i] = (padded[offset + i * 4] & 0xff) << 24 |
            (padded[offset + i * 4 + 1] & 0xff) << 16 |
            (padded[offset + i * 4 + 2] & 0xff) << 8 |
            (padded[offset + i * 4 + 3] & 0xff);
      }
      for (int i = 16; i < 80; i++) {
        w[i] = _leftRotate(w[i - 3] ^ w[i - 8] ^ w[i - 14] ^ w[i - 16], 1);
      }

      var a = h0, b = h1, c = h2, d = h3;

      for (int i = 0; i < 80; i++) {
        int f, k;
        if (i < 20) {
          f = (b & c) | ((~b) & d);
          k = 0x5a827999;
        } else if (i < 40) {
          f = b ^ c ^ d;
          k = 0x6ed9eba1;
        } else if (i < 60) {
          f = (b & c) | (b & d) | (c & d);
          k = 0x8f1bbcdc;
        } else {
          f = b ^ c ^ d;
          k = 0xca62c1d6;
        }
        final temp = (_leftRotate(a, 5) + f + d + k + w[i]) & 0xffffffff;
        d = c;
        c = b;
        b = _leftRotate(a, 30);
        a = temp;
      }

      h0 = (h0 + a) & 0xffffffff;
      h1 = (h1 + b) & 0xffffffff;
      h2 = (h2 + c) & 0xffffffff;
      h3 = (h3 + d) & 0xffffffff;
    }

    final result = Uint8List(16);
    for (int i = 0; i < 4; i++) {
      result[i] = (h0 >> (24 - i * 8)) & 0xff;
      result[i + 4] = (h1 >> (24 - i * 8)) & 0xff;
      result[i + 8] = (h2 >> (24 - i * 8)) & 0xff;
      result[i + 12] = (h3 >> (24 - i * 8)) & 0xff;
    }

    return result.map((b) => b.toRadixString(16).padLeft(2, '0')).join();
  }

  static int _leftRotate(int value, int shift) {
    return ((value << shift) | (value >> (32 - shift))) & 0xffffffff;
  }

  static Uint8List aesEncrypt(String text, String keyStr) {
    final key = utf8.encode(keyStr);
    final data = utf8.encode(text);
    final padded = _pkcs7Pad(data, 16);
    final result = <int>[];

    final roundKeys = _aesKeyExpansion(key);

    for (int offset = 0; offset < padded.length; offset += 16) {
      final block = padded.sublist(offset, offset + 16);
      final encrypted = _aesEncryptBlock(block, roundKeys);
      result.addAll(encrypted);
    }

    return Uint8List.fromList(result);
  }

  static String aesDecrypt(Uint8List data, String keyStr) {
    try {
      final key = utf8.encode(keyStr);
      final roundKeys = _aesKeyExpansion(key);
      final result = <int>[];

      for (int offset = 0; offset < data.length; offset += 16) {
        final block = data.sublist(offset, offset + 16);
        final decrypted = _aesDecryptBlock(block, roundKeys);
        result.addAll(decrypted);
      }

      final unpadded = _pkcs7Unpad(result, 16);
      return utf8.decode(unpadded);
    } catch (e) {
      return '';
    }
  }

  static Uint8List encryptParams(String path, String jsonParams) {
    final message = 'nobody${path}use${jsonParams}md5forencrypt';
    final digest = md5(message);
    final data = '$path-36cd479b6b5-$jsonParams-36cd479b6b5-$digest';
    return aesEncrypt(data, eapiKey);
  }

  static List<int> _pkcs7Pad(List<int> data, int blockSize) {
    final padLen = blockSize - (data.length % blockSize);
    return [...data, ...List.filled(padLen, padLen)];
  }

  static List<int> _pkcs7Unpad(List<int> data, int blockSize) {
    final padLen = data.last;
    if (padLen > 0 && padLen <= 16) {
      return data.sublist(0, data.length - padLen);
    }
    return data;
  }

